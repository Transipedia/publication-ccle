#!/usr/bin/env python3

"""
- Remove line that do not match any of our SRR
- Add new column with corresponding SRR from DepMap_ID (ACH-xxxxxx)
- Add new column with ratio x 100 of RNAseq_AC column (name: AF_%)
"""


import os
import sys
import argparse


__appname__   = "application_name"
__shortdesc__ = "Short description."
__licence__   = "none"
__version__   = "0.1.1"
__author__    = "Benoit Guibert <benoit.guibert@free.fr>"


DEPMAPID_FIELD = 'DepMap_ID'
DEPMAPID_NAME ='ACH-00'
AF_RATIO_FIELD = "RNAseq_AC"
AF = "AF_%"


def main():
    """ Function doc """
    args = usage()
    data = []
    warnings = set()
    ### Find the position of the DepMap ID column
    index_id = None         # position of the column containing the header DepMap ID
    index_af = None         # position of the column containing the header RNAseq_AC
    index_ishotsport = None # position of the column containing the header isCOSMIChotspot

    first_line = args.infile.readline().rstrip().split('\t')
    ### find index of isCOSMIChostpot
    is_cosmic_hotspot_id = first_line.index("isCOSMIChotspot")
    
    ### find position of DEPMAP ID (ACH...) and add column to header (if there header)
    if DEPMAPID_FIELD in first_line:
        ### Input file has a header
        index_id = first_line.index(DEPMAPID_FIELD)
        first_line.insert(index_id, 'SRR')
    else:
        sys.exit(f"Error: the header has no field called {DEPMAPID_FIELD!r}.")
    ### find position of RNAseq_AC and add column to header
    if AF_RATIO_FIELD in first_line:
        ### Input file has a header
        index_af = first_line.index(AF_RATIO_FIELD)+1
        first_line.insert(index_af, AF)
    else:
        sys.exit(f"Error: the header has no field called {AF_RATIO_FIELD!r}.")
    ### add header to data
    data.append('#{}\n'.format('\t'.join(first_line)))


    ### Select line when DepMap_id is found in SRR dict and add AF
    for line in args.infile:
        line_l = line.rstrip('\n').split('\t')
        ### Filter on "--is_cosmic_hotspot"
        if args.is_cosmic_hotspot and line_l[is_cosmic_hotspot_id] == "False":
            continue
        ### get DepMap_id, insert it as new field and add to data
        depmap_id = line_l[index_id]
        try:
            ### add SRR
            line_l.insert(index_id, SRR[depmap_id])
            ### add AF
            try:
                alt, ref = line_l[index_af-1].split(':')
                af = round(int(alt)/(int(alt)+int(ref))*100)
                line_l.insert(index_af, str(af))
            except ValueError:
                line_l.insert(index_af, '')
            ### keep row
            data.append('\t'.join(line_l)+'\n')
        except KeyError:
            pass



    ### print to data to output file
    if args.outfile:
        outfile = args.outfile
    elif args.infile.name == '<stdin>' or args.infile.name.startswith('/dev/fd/'):
        outfile = open('outfile_withSRR.tsv', 'w')
    else:
        outfile, ext = os.path.splitext(os.path.basename(args.infile.name))
        outfile = f"{outfile}_withSRR{ext}"
        outfile = open(outfile, 'w')

    outfile.write(''.join(data))

    ### prompt output
    if os.path.isfile(outfile.name):
        print(f"File {outfile.name} has been created.")
    if warnings:
        print(f"Some DepMap ID no matching with SRR ({len(warnings)}):")
        for i,ach in enumerate(warnings):
            print(f"  {ach}")
            if i == 15:
                print('  ... (more)')
                break




def usage():
    """
    Help function with argument parser.
    https://docs.python.org/3/howto/argparse.html?highlight=argparse
    """
    doc_sep = '=' * min(72, os.get_terminal_size(2)[0])
    parser = argparse.ArgumentParser(description= f'{doc_sep}{__doc__}{doc_sep}',
                                     formatter_class=argparse.RawDescriptionHelpFormatter,)
    ### OPTION
    parser.add_argument("infile",
                        help="file or stdin",
                        type=argparse.FileType('r'),
                        nargs='?' if sys.stdin else 1,
                        default=sys.stdin,
                        metavar=('input_file'),
                       )
    parser.add_argument("-o", "--outfile",
                        help=("output file name"),
                        type=argparse.FileType('w'),
                        metavar="output_file",
                       )
    parser.add_argument("--is-cosmic-hotspot",
                        action='store_true'
                       )
    parser.add_argument('-v', '--version',
                        action='version',
                        version=f"{parser.prog} v{__version__}",
                       )
    ### Go to "usage()" without arguments or stdin
    if len(sys.argv) == 1 and sys.stdin.isatty():
        parser.print_help()
        sys.exit()
    return parser.parse_args()


